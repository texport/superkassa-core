package kz.mybrain.superkassa.core.data.exception

/**
 * Исключение, указывающее на нарушение протокола взаимодействия с ОФД.
 *
 * Данная ошибка возникает, когда ответ от сервера ОФД не соответствует ожидаемому формату,
 * содержит некорректную структуру данных или не может быть успешно десериализован.
 * Повторные попытки с теми же данными обычно не приводят к успеху без изменения данных или протокола.
 *
 * @param message Детальное сообщение об ошибке.
 * @param cause Первопричина исключения (если имеется).
 */
class OfdProtocolException(
    message: String,
    cause: Throwable? = null
) : RuntimeException(message, cause)
