package kz.mybrain.superkassa.core.domain.model.kkm

/**
 * Текущее состояние кассы (ККМ).
 */
enum class KkmState {
    /** Касса проинициализирована и готова к работе (смена закрыта). */
    IDLE,
    /** Касса активна, смена открыта, готова к оформлению чеков. */
    ACTIVE,
    /** Касса находится в режиме программирования/настройки. */
    PROGRAMMING,
    /** Работа кассы заблокирована (например, смена превысила 24 часа или исчерпан лимит автономности). */
    BLOCKED
}
