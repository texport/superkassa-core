package kz.mybrain.superkassa.core.domain.model.report

/**
 * Тип печатного документа.
 * Один набор эндпоинтов (print/html, print/pdf) поддерживает все варианты документов.
 */
enum class PrintDocumentType {
    /**
     * Чек, внесение или изъятие по идентификатору документа (тип определяется по данным в БД).
     */
    DOCUMENT,

    /**
     * X-отчёт по текущей открытой смене (требует только kkmId).
     */
    X_REPORT,

    /**
     * Печатная форма открытия смены (текущая открытая смена, требует только kkmId).
     */
    OPEN_SHIFT,

    /**
     * Z-отчёт по смене (закрытие смены, требует kkmId и shiftId).
     */
    CLOSE_SHIFT
}
