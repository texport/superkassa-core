package kz.mybrain.superkassa.core.domain.model.delivery

import kotlinx.serialization.Serializable

/**
 * Статус доставки документа в ОФД с точки зрения ядра (статусы попыток доставки чеков).
 */
@Serializable
enum class DeliveryStatus {
    /**
     * Документ успешно доставлен в ОФД в режиме реального времени и подтвержден.
     */
    ONLINE_OK,

    /**
     * Документ отправлен в ОФД в реальном времени, связь была, но ОФД вернул ошибку обработки.
     * Документ НЕ отправляется в офлайн-очередь; требуется исправление данных и повторная отправка.
     */
    ONLINE_ERROR,

    /**
     * Документ сохранен локально и помещен в офлайн-очередь для последующей отправки (автономный режим).
     */
    OFFLINE_QUEUED,

    /**
     * Документ не был отправлен в ОФД и не поставлен в очередь из-за критической внутренней ошибки.
     */
    NOT_SENT
}
