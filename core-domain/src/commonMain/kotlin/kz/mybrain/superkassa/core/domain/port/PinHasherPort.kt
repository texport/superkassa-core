package kz.mybrain.superkassa.core.domain.port

/**
 * Порт для хеширования PIN-кодов пользователей ККМ (кассиров, администраторов).
 *
 * Позволяет абстрагировать алгоритм одностороннего шифрования (хеширования)
 * от логики домена и изменять его реализацию при необходимости без влияния на бизнес-правила.
 */
interface PinHasherPort {

    /**
     * Хеширует PIN-код пользователя ККМ для безопасного хранения в БД.
     *
     * @param pin исходный PIN-код пользователя.
     * @return строка с криптографическим хешем PIN-кода (например, в формате HEX).
     */
    fun hash(pin: String): String
}
