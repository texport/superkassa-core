package kz.mybrain.superkassa.core.domain.port

import kz.mybrain.superkassa.core.domain.model.queue.OfflineQueueCommandRequest

/**
 * Порт для управления автономной (офлайн) очередью фискальных команд ККМ.
 *
 * Офлайн-очередь выступает единственным источником истины о доступности сети для кассы.
 * Ядро системы обращается к методу [canSendDirectly] перед прямой отправкой документа в ОФД.
 */
interface OfflineQueuePort {

    /**
     * Проверяет, разрешено ли отправлять документ напрямую в ОФД для указанной кассы.
     *
     * Если возвращает `false`, значит у кассы есть неотправленные офлайн-документы (автономная очередь),
     * и новый документ должен быть помещён в очередь с помощью [enqueueOffline] во избежание нарушения хронологии.
     *
     * @param kkmId уникальный идентификатор ККМ.
     * @return `true`, если прямая отправка разрешена; `false`, если требуется отправка через очередь.
     */
    fun canSendDirectly(kkmId: String): Boolean

    /**
     * Добавляет фискальную команду в офлайн-очередь для последующей фоновой отправки.
     *
     * @param command данные фискальной команды [OfflineQueueCommandRequest] для постановки в очередь.
     * @return `true`, если команда успешно добавлена; `false` в противном случае.
     */
    fun enqueueOffline(command: OfflineQueueCommandRequest): Boolean

    /**
     * Удаляет все отложенные команды в очереди для указанной кассы.
     *
     * @param kkmId уникальный идентификатор ККМ.
     * @return `true`, если очередь успешно очищена; `false` в противном случае.
     */
    fun deleteQueuedCommands(kkmId: String): Boolean

    /**
     * Запускает обработку пакета отложенных команд из офлайн-очереди для указанной ККМ.
     * Отправляет документы в ОФД в фоновом режиме.
     *
     * @param kkmId уникальный идентификатор ККМ.
     * @param limit максимальное количество команд для обработки за один вызов.
     * @return количество успешно обработанных и отправленных команд.
     */
    fun processOfflineBatch(kkmId: String, limit: Int = 10): Int
}
