package kz.mybrain.superkassa.core.domain.model

import kotlinx.serialization.Serializable

/**
 * Статус доставки документа в ОФД с точки зрения ядра.
 */
@Serializable
enum class DeliveryStatus {
    /**
     * Команда успешно доставлена в ОФД и подтверждена.
     */
    ONLINE_OK,

    /**
     * Команда отправлена в ОФД, связь была, но ОФД вернул ошибку (не success).
     * Например: неверная структура запроса (протокол), некорректные данные, блокировка и т.п.
     * Документ НЕ попадает в офлайн-очередь — повторить нужно с исправленными данными.
     */
    ONLINE_ERROR,

    /**
     * Документ считается автономным: команда не отправлена в ОФД сразу и
     * должна быть доставлена позже (через очередь/офлайн-механику).
     */
    OFFLINE_QUEUED,

    /**
     * Команда не была отправлена в ОФД и не поставлена в очередь из-за внутренней ошибки.
     */
    NOT_SENT
}

